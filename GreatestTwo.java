public class GreatestTwo {
    public static void main(String[] args) {
        int num1=10,num2=90;
        if(num1==num2){
            System.out.println("equal");
        }else if(num1>num2){
            System.out.println(num1+" is greater");
        }else{
            System.out.println(num2+" is greater");
        }
    }
}
